#include "libft.h"

t_list_node	*ft_lstnew(void *content)
{
	t_list_node	*list;

	list = (t_list_node *)malloc(sizeof(t_list_node));
	if (!list)
		return (NULL);
	list->content = content;
	list->next = NULL;
	return (list);
}
